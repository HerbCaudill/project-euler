import { permutations } from '../../src/lib/permutations'

describe('permutations', () => {
  test.each`
    source    | perms
    ${'0'}    | ${'0'}
    ${'01'}   | ${'01,10'}
    ${'10'}   | ${'01,10'}
    ${'012'}  | ${'012,021,102,120,201,210'}
    ${'120'}  | ${'012,021,102,120,201,210'}
    ${'0123'} | ${'0123,0132,0213,0231,0312,0321,1023,1032,1203,1230,1302,1320,2013,2031,2103,2130,2301,2310,3012,3021,3102,3120,3201,3210'}
    ${'3021'} | ${'0123,0132,0213,0231,0312,0321,1023,1032,1203,1230,1302,1320,2013,2031,2103,2130,2301,2310,3012,3021,3102,3120,3201,3210'}
  `('$source', ({ source, perms }) => {
    const actual = permutations(source.split(''))
    const expected = perms.split(',').map((s: string) => s.split(''))
    expect(actual).toEqual(expected)
  })
})
